// C++ Program to illustrate the relational operators 
#include <iostream> 
using namespace std; 

int main() 
{ 
	// variables for comparison 
	int a = 10; 
	int b = 6; 

	// greater than 
	cout << "a > b = " << (a > b) << endl; 
	
	// less than 
	cout << "a < b = " << (a < b) << endl; 
	
	// equal to 
	cout << "a == b = " << (a == b) << endl; 
	
	// not equal to 
	cout << "a != b = " << (a != b) << endl; 

	return 0; 
}
